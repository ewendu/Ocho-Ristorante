function runFormValidation()
{
    var $form;
    
    $form = $('form:not([data-no-validation=true])');
    
    if($form.length == 1)
    {
        var formValidator = new FormValidator($form);
        formValidator.run();
    }
}






/////////////////////////////////////////////////////////////////////////////////////////
// CODE PRINCIPAL                                                                      //
/////////////////////////////////////////////////////////////////////////////////////////

$(function()
{
    runFormValidation();
  
    
});